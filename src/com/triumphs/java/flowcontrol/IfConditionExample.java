package com.triumphs.java.flowcontrol;

public class IfConditionExample {

	public static void main(String[] args) {
		int number = 10;

		// checks if number is in tens
		if (number > 9) {
			System.out.println("The number is in tens.");
		}

		System.out.println("Statement outside if block");
		
		// create a string 
	    String sportsperson = "Rohit Sharma";

	    // if statement
	    if (sportsperson == "Neeraj Chopra") {
	      System.out.println(sportsperson + " Is my favourite sportsperson. ");
	    }
	}

}
