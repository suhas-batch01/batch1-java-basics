package com.triumphs.java.datatypes;

public class DataTypes {

	public static void main(String[] args) {
		boolean isValuePresent = true;
		System.out.println("boolean value:" + isValuePresent); // prints true

		byte byteVal;
		byteVal = 111;
		System.out.println("byte value: " + byteVal); // prints 111

		short temperature;
		temperature = -159;
		System.out.println("short value:" + temperature); // prints -159

		int annualSalary = 2250000;
		System.out.println("int value:" + annualSalary); // print -2250000

		long range = -42332200000L;
		System.out.println("long value:" + range); // prints -42332200000

		double number = -180.3;
		System.out.println("double value:" + number); // prints -180.3

		float floatVal = -30.3f;
		System.out.println("float value:" + floatVal); // prints -30.3

		char letter1 = '9';
		System.out.println("char value1:" + letter1); // prints 9
		char letter2 = 65;
		System.out.println("char value2:" + letter2); // prints A

		String myString = "Java Programming";
		System.out.println("string value:" + myString);
		
	}

}
